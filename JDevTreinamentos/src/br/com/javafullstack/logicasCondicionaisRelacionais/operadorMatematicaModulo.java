package br.com.javafullstack.logicasCondicionaisRelacionais;

public class operadorMatematicaModulo {
	
	public static void main(String[] args) {
		
		/*
		 * O módulo "%", mostra o resto de uma divisão  
		 * *
		*/
		
		
		double carro = 9;
		double pessoa = 2;
		double resto = carro % pessoa;
		
		System.out.println("Sobraram exatamente: " + resto + " Carros.");
	}

}
